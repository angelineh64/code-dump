# Program asks the user the radius of their circle.
pi = (3.14)  # Defines what variable 'pi' is
r = input("Please type radius of your circle:")  # User should enter the radius of their circle.
r = float(r)  # Allows for decimal place float variables r, r being the radius of the user's circle.
area = pi * r ** 2  # Program will calculate the area by using pi x r^2

print("Area of the circle is:", area)  # Prints the area of the circle.
